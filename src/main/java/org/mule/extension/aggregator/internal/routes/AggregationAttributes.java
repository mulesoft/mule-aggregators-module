package org.mule.extension.aggregator.internal.routes;


/**
 * Attributes with information regarding the containing aggregator and the aggregated elements
 *
 * @since 1.0
 */
public class AggregationAttributes {

  /**
   * The aggregation Id. It's value comes from the event's message correlationId.
   * If the aggregator does not aggregate by correlationId, then this field will be an autogenerated value for the
   * current aggregated group.
   */
  private String aggregationId;

  /**
   * The time in which the first aggregated value arrived.
   */
  private long firstItemArrivalTime;

  /**
   * The time in which the last aggregated element arrived.
   */
  private long lastItemArrivalTime;

  /**
   * A boolean value indicating if the aggregated group is complete
   */
  private boolean isGroupComplete;

  public AggregationAttributes() {
    this.isGroupComplete = false;
  }

  public AggregationAttributes(String aggregationId, long firstItemArrivalTime, long lastItemArrivalTime,
                               boolean isGroupComplete) {
    this.aggregationId = aggregationId;
    this.firstItemArrivalTime = firstItemArrivalTime;
    this.lastItemArrivalTime = lastItemArrivalTime;
    this.isGroupComplete = isGroupComplete;
  }

  public void setAggregationId(String aggregationId) {
    this.aggregationId = aggregationId;
  }

  public void setFirstItemArrivalTime(long firstItemArrivalTime) {
    this.firstItemArrivalTime = firstItemArrivalTime;
  }

  public void setLastItemArrivalTime(long lastItemArrivalTime) {
    this.lastItemArrivalTime = lastItemArrivalTime;
  }

  public void setGroupComplete() {
    isGroupComplete = true;
  }

  public String getAggregationId() {
    return aggregationId;
  }

  public long getFirstItemArrivalTime() {
    return firstItemArrivalTime;
  }

  public long getLastItemArrivalTime() {
    return lastItemArrivalTime;
  }

  public boolean isGroupComplete() {
    return isGroupComplete;
  }
}
